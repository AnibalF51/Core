     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $nEmpresa = new Empresa();

        $nEmpresa->nombreE = $request->nEmpresa;
        $nEmpresa->descripcion = $request->descripcion;
        $nEmpresa->nit = $request->nit;
        $nEmpresa->nombreP = $request->nombre;
        $nEmpresa->estado = $request->txtestado;
        $nEmpresa->save();
        return redirect()->route('empresa.lista');
    }

